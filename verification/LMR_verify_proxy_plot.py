                plt.ylabel("Probability density",fontweight='bold')
                xmin,xmax,ymin,ymax = plt.axis()
                ymin = 0.0
                #ymax = 0.45
                #ymax = 0.1 # for r_crit = 0.2
                #ymax = 0.5; nbins = 5
                ymax = 12.0; nbins = 6        

