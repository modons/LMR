                    xpos = xmin+0.025*(xmax-xmin)
                    ypos = ymin+0.75*(ymax-ymin)
                    plt.text(xpos,ypos,'  p-value = %s' %"{:.3f}".format(dist_test.pvalue),fontsize=9,fontweight='bold')

