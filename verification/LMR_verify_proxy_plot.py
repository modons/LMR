                prior_tmp = []
                stat_comp = []
                for p in range(nbperiods):
                    # pick right dict and associate to "workdict"
                    dname = v+'_dict'
                    workdict = eval(dname)
                    sitetag = list(workdict[p].keys())
                    if proxy == 'All':
                        proxy_types = list(set([item[0] for item in sitetag]))
                    else:
                        proxy_types = proxy

