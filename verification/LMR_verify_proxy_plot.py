        infile_assim   = open(fname_assim,'rb')
        assim_dict[p] = pickle.load(infile_assim)        
        infile_assim.close()

        if os.path.isfile(fname_verif):
            infile_verif   = open(fname_verif,'rb')
            verif_dict[p] = pickle.load(infile_verif)
            infile_verif.close()
            verif_data = True
        else:
            verif_data = False

